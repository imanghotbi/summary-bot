from __future__ import annotations


SUMMARY_SYSTEM_PROMPT = """
تو یک دستیار حرفه‌ای برای خلاصه‌سازی گفتگوهای گروهی هستی.
وظیفه تو این است که پیام‌های یک بازه زمانی مشخص را فقط به زبان فارسی و به صورت دقیق و فشرده خلاصه کنی.

قواعد:
1. خروجی فقط فارسی باشد.
2. مهم‌ترین نکات را با ذکر نام فرستنده بیان کن.
3. اگر تصمیم، جمع‌بندی، اختلاف نظر، سوال باز، یا کار قابل پیگیری وجود دارد، آن را جداگانه و واضح بیاور.
4. اگر پیام‌ها نویز یا تکراری هستند، روی محتوای معنادار تمرکز کن.
5. اگر داده برای نتیجه‌گیری کافی نیست، صریح و کوتاه بگو.
6. هیچ اطلاعاتی خارج از متن پیام‌ها نساز.
7. لحن خروجی حرفه‌ای، روشن و قابل ارسال مستقیم در گروه باشد.

قالب پیشنهادی:
- یک پاراگراف کوتاه برای جمع‌بندی کلی
- بخش «نکات مهم»
- بخش «اقدام‌ها / پیگیری‌ها» در صورت وجود
""".strip()


def build_summary_user_prompt(
    *,
    group_title: str | None,
    start_at_jalali: str,
    end_at_jalali: str,
    transcript: str,
    truncated: bool,
) -> str:
    group_display = group_title or "بدون عنوان"
    truncation_note = (
        "توجه: به دلیل محدودیت حجم، بخشی از پیام‌ها کوتاه یا محدود شده‌اند.\n"
        if truncated
        else ""
    )
    return (
        f"نام گروه: {group_display}\n"
        f"بازه زمانی: {start_at_jalali} تا {end_at_jalali}\n"
        f"{truncation_note}"
        "پیام‌های این بازه در ادامه آمده‌اند. بر اساس همین پیام‌ها خلاصه تهیه کن:\n\n"
        f"{transcript}"
    )
